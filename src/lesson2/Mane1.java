package lesson2;

public class Mane1 {
    public static void main(String[] args) {
        int mark = 30;

        if (mark == 2) {
            System.out.println("Плохо! Иди учись!");
        } else if (mark == 3) {
            System.out.println("Не очень, ну да ладно.");
        } else if (mark == 4) {
            System.out.println("Хорошо");

        } else if (mark == 5) {
            System.out.println("Отлично! Ты молодец.");
        } else {
            System.out.println("Хм.. это не оценка!");
        }

    }

}
